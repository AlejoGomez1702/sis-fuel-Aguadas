    public void initInformation()
    {
        this.combVehicles.removeAllItems();
        int numFuels = this.vehicles.size();
        Vehicle aux;
        
        for(int i = 0; i < numFuels; i++) 
        {
            aux = this.vehicles.get(i);
            this.combVehicles.addItem(aux.getId() + "# " + aux.getDescription());
        }
    }
    
    public void bindingModel(Expense expense)
    {
        this.expense = expense;
        this.lblTitle.setText("Modificar Gasto");
        String vehicleSelected = expense.getVehicle().getId() + "# " + expense.getVehicle()
                                                            .getDescription();
System.out.println("Vehiculo ==> " + vehicleSelected);
        this.combVehicles.setSelectedItem(vehicleSelected);
        this.dateChoss.setDate(this.calendar.convertStringToDate(expense.getDate()));
        this.txtInvoiceCode.setText(expense.getInvoiceCode());
        double countGal = expense.getValue() / (expense.getVehicle().getFuel().getValue());
        this.txtCountGal.setText(countGal + "");        
    }

