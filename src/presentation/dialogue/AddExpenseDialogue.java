    private void btnAddExpenseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddExpenseActionPerformed
       String vehicle = (String)this.combVehicles.getSelectedItem();
       Calendar date = this.dateChoss.getCalendar();
       String invoiceCode = this.txtInvoiceCode.getText();
       
       //String value = this.txtValue.getText();
       
       if(this.expense == null) //Nuevo gasto.
       {
           boolean check = this.controller.getExpenseController().
                                    create(vehicle, date, invoiceCode, this.txtCountGal);

            if(check)
            {
                JOptionPane.showMessageDialog(this, "Gasto Agregado Correctamente!");
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "ERROR, Compruebe Los Datos Ingresados");
            }
       }
       else  //Modificar gasto.
       {
           boolean check = this.controller.getExpenseController().update
                    (this.expense, vehicle, date, invoiceCode, this.txtCountGal);
           if(check)
           {
               JOptionPane.showMessageDialog(this, "Gasto Actualizado Correctamente");
               this.dispose();
           }
           else
           {
               JOptionPane.showMessageDialog(this, "ERROR, Verifique Los Datos Ingresados");
           }           
       } 
    }//GEN-LAST:event_btnAddExpenseActionPerformed

