    public void initInformation()
    {
        this.combFuels.removeAllItems();
        int numFuels = this.fuels.size();
        Fuel aux;
        
        for(int i = 0; i < numFuels; i++) 
        {
            aux = this.fuels.get(i);
            this.combFuels.addItem(aux.getId() + "# " + aux.getName());
        }
    }
    
    public int getIdFromComb(String selected)
    {
        String[] info = selected.split("#");
        int id = -1;
        try {
            id = Integer.parseInt(info[0]);
        } catch (Exception e) {
        }
        
        return id;
    }

