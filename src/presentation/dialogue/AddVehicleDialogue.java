    private void btnAddVehicleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddVehicleActionPerformed
        String description = this.txtDescription.getText();
        String fuel = (String)this.combFuels.getSelectedItem();
        int id = this.getIdFromComb(fuel);
        if(id > 0)
        {
            if(this.vehicle == null)//Si se desea crear.
            {
                boolean flag = this.controller.getVehicleController().create(description, id);
                if(flag)
                {
                    JOptionPane.showMessageDialog(this, "Vehículo Agregado Correctamente!");
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Error, Compruebe Los Datos Ingresados");
                }
            }
            else
            {
                boolean check = this.controller.getVehicleController().update(this.vehicle, description, id);
                if(check)
                {
                    JOptionPane.showMessageDialog(this, "Vehículo Actualzado Correctamente!");
                    this.dispose();
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Error, Compruebe Los Datos Ingresados");
                }
            }
            
        }

    }//GEN-LAST:event_btnAddVehicleActionPerformed

    public void bindingModel(Vehicle vehicle)
    {
        this.lblTitle.setText("Modificar Vehículo");
        this.vehicle = vehicle;
        this.txtDescription.setText(vehicle.getDescription());
        String fuel = vehicle.getFuel().getId() + "# " + vehicle.getFuel().getName();
        this.combFuels.setSelectedItem(fuel);
        //this.txtValue.setText((int) fuel.getValue() + "");        
    }
    
    
